String[] header = {"ID", "Nome", "Quantidade"};

// Mostrar tabela de produtos
public void mostrarTabela(){
    try{
        DefaultTableModel modeloTabela = new DefaultTableModel(header, 0);
        TableProdutos.setModel(modeloTabela);
        modeloTabela.setRowCount(0);

        ProdutoDao dao = new ProdutoDao();
        List<Produto> lista = dao.listar();

        for (Produto p : lista) {
            modeloTabela.addRow(new Object[]{p.getId(), p.getNome(), p.getQtd()});
        }
    } catch(Exception e){
        JOptionPane.showMessageDialog(null, "Erro ao carregar produtos: " + e.getMessage());
    }
}

public void limparCampos(){
    txtNome.setText("");
    txtQtd.setText("");
}
public boolean validarCampos(){
    if (txtNome.getText().isEmpty() || txtQtd.getText().isEmpty()) {
        JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigatórios!");
        return false;
    }
    return true;
}
public void selecionarProduto(){
    int linhaTabela = TableProdutos.getSelectedRow();
    if (linhaTabela != -1) {
        txtNome.setText(TableProdutos.getValueAt(linhaTabela, 1).toString());
        txtQtd.setText(TableProdutos.getValueAt(linhaTabela, 2).toString());
    } else {
        JOptionPane.showMessageDialog(null, "Selecione um produto na tabela para editar.");
    }
}






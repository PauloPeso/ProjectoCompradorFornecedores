    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        // TODO add your handling code here:
        if (produtoSelecionadoId == -1) {
        JOptionPane.showMessageDialog(null, "Selecione um produto na tabela para eliminar.");
        return;
    }

    int opcao = JOptionPane.showConfirmDialog(null,
            "Tem certeza que deseja eliminar este produto?",
            "Confirmação",
            JOptionPane.YES_NO_OPTION);

    if (opcao == JOptionPane.YES_OPTION) {
        ProdutoDao dao = new ProdutoDao();

        try {
            dao.excluir(produtoSelecionadoId); // Método que você vai criar no DAO
            JOptionPane.showMessageDialog(null, "Produto eliminado com sucesso!");

            mostrarTabela(); // Atualiza a JTable
            produtoSelecionadoId = -1; // Reseta ID selecionado
            txtNome.setText("");
            txtQtd.setText("");

        } catch (SQLException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(null, "Erro ao eliminar produto.");
        }
    }

    }//GEN-LAST:event_btnExcluirActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        // TODO add your handling code here:
        String nome = txtNome.getText();
    int qtd = Integer.parseInt(txtQtd.getText());

    if (produtoSelecionadoId == -1) {
        JOptionPane.showMessageDialog(null, "Selecione um produto da tabela para atualizar.");
        return;
    }

    ProdutoDao dao = new ProdutoDao();
    try {
        Produto p = new Produto();
        p.setId(produtoSelecionadoId); // Usa o ID salvo ao clicar na tabela
        p.setNome(nome);
        p.setQtd(qtd);

        dao.atualizar(p);
        JOptionPane.showMessageDialog(null, "Produto atualizado!");

        mostrarTabela(); // Atualiza a tabela com os dados atualizados
        produtoSelecionadoId = -1; // Limpa a seleção

        txtNome.setText(""); // Limpa os campos de texto
        txtQtd.setText("");

    } catch (SQLException ex) {
        ex.printStackTrace();
        JOptionPane.showMessageDialog(null, "Erro ao atualizar!");
    }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void TableProdutosMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableProdutosMouseClicked
        // TODO add your handling code here:
        int linhaSelecionada = TableProdutos.getSelectedRow(); // Pega a linha clicada

    if (linhaSelecionada != -1) {
        produtoSelecionadoId = (int) TableProdutos.getValueAt(linhaSelecionada, 0); // ID está na coluna 0
        String nome = (String) TableProdutos.getValueAt(linhaSelecionada, 1);        // Nome na coluna 1
        int qtd = (int) TableProdutos.getValueAt(linhaSelecionada, 2);              // Qtd na coluna 2

        txtNome.setText(nome);                   // Preenche o campo Nome
        txtQtd.setText(String.valueOf(qtd));     // Preenche o campo Quantidade
    }
    }//GEN-LAST:event_TableProdutosMouseClicked


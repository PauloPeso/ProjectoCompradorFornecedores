    // Tabela de fornecedores
    public void mostrarTabela(){
        try{
            DefaultTableModel modeloTabela = new DefaultTableModel(header, 0);
            TableFornecedores.setModel(modeloTabela);
            modeloTabela.setRowCount(0);
            List<Fornecedor> lista = dao.listar();
            for (Fornecedor f : lista) {
                modeloTabela.addRow(new Object[]{f.getId(), f.getNome(), f.getNIF(), f.getEmail()});
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
    //limpar campos
    public void limparCampos(){
        TxtNome.setText("");
        TxtNif.setText("");
        txtEmail.setText("");
    }
    //validar campos checar se os campos estam vazios
    public boolean validarCampos(){
        if(TxtNome.getText().isEmpty() || TxtNif.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Preencha todos os campos obrigat√≥rios!");
            return false;
        }
        else{
            return true;
        }
    }
    //metodo para selecionar fornecedor na tabela e preencher os campos
    public void selecionarFornecedor(){
        int linhaTabela = TableFornecedores.getSelectedRow();
        if(linhaTabela != - 1){
            TxtNome.setText(TableFornecedores.getValueAt( linhaTabela,1).toString());
            TxtNif.setText(TableFornecedores.getValueAt( linhaTabela,2).toString());
            txtEmail.setText(TableFornecedores.getValueAt( linhaTabela,3).toString());
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um fornecedor na tabela para editar.");
        }
    }
   


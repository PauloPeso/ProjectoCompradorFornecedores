    private void BtnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BtnSalvarActionPerformed
        // TODO add your handling code here:
        Fornecedor f = new Fornecedor();
        f.setNome(TxtNome.getText());
        f.setNIF(TxtNif.getText());
        f.setEmail(txtEmail.getText());
        // Aqui voc√™ deve chamar o m√©todo de salvar do DAO
        if (validarCampos() == false) {
             // Se os campos n√£o forem v√°lidos, n√£o prossegue com o salvamento   
            
        }
        else{
            if (!dao.VerificaNIF(TxtNif.getText())) {
                JOptionPane.showMessageDialog(null, "NIF j√° cadastrado!");
            } else {
                try {
                dao.salvar(f);
                JOptionPane.showMessageDialog(null, "Fornecedor cadastrado com sucesso!");
                mostrarTabela();
                limparCampos();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao cadastrar fornecedor: " + e.getMessage());
                }
            }
        }
        
        

        
    }//GEN-LAST:event_BtnSalvarActionPerformed

    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int linha = TableFornecedores.getSelectedRow();
           try {
               if (linha != -1) {
                   int id = (int) TableFornecedores.getValueAt(linha, 0);
                   dao.excluir(id);
                   JOptionPane.showMessageDialog(null, "Fornecedor exclu√≠do com sucesso!");
                   limparCampos();
                   mostrarTabela();
               } else {
                   JOptionPane.showMessageDialog(null, "Selecione um fornecedor para excluir.");
               }
           } catch (Exception e) {
               JOptionPane.showMessageDialog(null, "Erro ao excluir fornecedor: " + e.getMessage());
           }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
         int linha = TableFornecedores.getSelectedRow();
            if (linha != -1) {
                Fornecedor f = new Fornecedor();
                f.setId((int) TableFornecedores.getValueAt(linha, 0));
                f.setNome(TxtNome.getText());
                f.setNIF(TxtNif.getText());
                f.setEmail(txtEmail.getText());
                new FornecedorDao().atualizar(f);
                JOptionPane.showMessageDialog(null, "Fornecedor atualizado com sucesso!");
                mostrarTabela();
                limparCampos();
            } else {
                JOptionPane.showMessageDialog(null, "Selecione um fornecedor para editar.");
            }
    }//GEN-LAST:event_jButton1ActionPerformed

